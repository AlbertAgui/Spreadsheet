package org.example;

public class Cell {
    public String content;
    public float value;

    public void setContent(String content) {
        this.content = content;
    }

    public void setValue(float value) {
        this.value = value;
    }

    public float getValue() {
        return value;
    }

    public String getContent() {
        return content;
    }
}
